const { rejects } = require('assert');
const { Builder, By, Key, until } = require('selenium-webdriver');
fs = require('fs');

async function example(data) {
    let driver = await new Builder().forBrowser('chrome').build();
    try {
        for (let el of data) {
            let suggestionResults = ""
            try {
                console.log(data.indexOf(el), data.length)
                await driver.get('https://www.google.com/maps/');
                await driver.findElement(By.name('q')).sendKeys(`${el.lat},${el.lng}`, Key.RETURN);
                suggestionResults = await driver.wait(until.elementLocated(By.css("span[jsan='7.widget-pane-link']")));
                suggestionResults = await (await driver.findElements(By.css("span[jsan='7.widget-pane-link']")))[1].getText()
            } catch (e) {
                continue
            }
            let state = ""
            suggestionResults = suggestionResults.split(" ").splice(1).join(" ")
            el.suggestionResults = suggestionResults
            if (suggestionResults.split(" - ").length > 1) {
                suggestionResults = suggestionResults.split(" - ")
                state = suggestionResults[1]
                suggestionResults = suggestionResults.slice(0, -1).join("")
            } else if (suggestionResults.split(", ").length > 1) {
                console.log(suggestionResults, ", ")
                suggestionResults = suggestionResults.split(", ")
                state = suggestionResults[1]
                suggestionResults = suggestionResults.slice(0, -1).join("")
            }
            let address = suggestionResults
            el.address = address
            el.state = state
        }
        fs.writeFile('out-data.json', JSON.stringify(data, null, 2), function (err) {
            if (err) return console.log(err);
            console.log('out-data.json writed');
        });
    } finally {
        await driver.quit();
    }
}

async function readData() {
    return new Promise((resolve, rejects) => {
        fs.readFile('./data.json', 'utf8', (err, data) => {
            if (err) {
                rejects(err)
            }
            resolve(data)
        })
    })
}

async function main() {
    var data = JSON.parse(await readData());
    example(data);
}


